# Data Types
# 1. String
# prints to the console the character at index 4
print("Hello"[4])
print("123" + "456")

# 2. Integer
print(123 + 456)
print(123_456_789)

# 3. Float
print(3.12534)

# 4. Boolean
print(True)
print(False)

# Type Checking
name = "Madu"
num1 = 123
print(type(name))
print(type(num1))

# Type Conversion or Casting
print(str(123) + str(456))
print(float(70))

